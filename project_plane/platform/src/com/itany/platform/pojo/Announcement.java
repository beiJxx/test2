package com.itany.platform.pojo;

import java.util.Date;


public class Announcement  implements java.io.Serializable {



     /**
     * serialVersionUID
     */
    private static final long serialVersionUID = 1L;
    private Integer annId;
     private SysUser sysUser;
     private String annTitile;
     private String annText;
     private Date annCretime;


    public Announcement() {
    }

    public Announcement(String annTitile, String annText, Date annCretime) {
        this.annTitile = annTitile;
        this.annText = annText;
        this.annCretime = annCretime;
    }
    
    public Announcement(SysUser sysUser, String annTitile, String annText, Date annCretime) {
        this.sysUser = sysUser;
        this.annTitile = annTitile;
        this.annText = annText;
        this.annCretime = annCretime;
    }

   

    public Integer getAnnId() {
        return this.annId;
    }
    
    public void setAnnId(Integer annId) {
        this.annId = annId;
    }

    public SysUser getSysUser() {
        return this.sysUser;
    }
    
    public void setSysUser(SysUser sysUser) {
        this.sysUser = sysUser;
    }

    public String getAnnTitile() {
        return this.annTitile;
    }
    
    public void setAnnTitile(String annTitile) {
        this.annTitile = annTitile;
    }

    public String getAnnText() {
        return this.annText;
    }
    
    public void setAnnText(String annText) {
        this.annText = annText;
    }

    public Date getAnnCretime() {
        return this.annCretime;
    }
    
    public void setAnnCretime(Date annCretime) {
        this.annCretime = annCretime;
    }


}