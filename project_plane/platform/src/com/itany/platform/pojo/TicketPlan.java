package com.itany.platform.pojo;

import java.util.Date;



public class TicketPlan  implements java.io.Serializable {



     /**
     * serialVersionUID
     */
    private static final long serialVersionUID = 1L;
    private int tpId;
     private Distributors distributors;
     private DistributionPlan distributionPlan;
     private Date takeOffDate;
     private String planName;
     private int firstNum;
     private int economyNum;
     private int firstSaleNum;
     private int economySaleNum;


    public TicketPlan() {
    }

    public TicketPlan(Date takeOffDate, String planName) {
        this.takeOffDate = takeOffDate;
        this.planName = planName;
    }
    
    public TicketPlan(Distributors distributors, DistributionPlan distributionPlan, Date takeOffDate, String planName, short firstNum, short economyNum, short firstSaleNum, short economySaleNum) {
        this.distributors = distributors;
        this.distributionPlan = distributionPlan;
        this.takeOffDate = takeOffDate;
        this.planName = planName;
        this.firstNum = firstNum;
        this.economyNum = economyNum;
        this.firstSaleNum = firstSaleNum;
        this.economySaleNum = economySaleNum;
    }

   
    public int getTpId() {
        return this.tpId;
    }
    
    public void setTpId(int tpId) {
        this.tpId = tpId;
    }

    public Distributors getDistributors() {
        return this.distributors;
    }
    
    public void setDistributors(Distributors distributors) {
        this.distributors = distributors;
    }

    public DistributionPlan getDistributionPlan() {
        return this.distributionPlan;
    }
    
    public void setDistributionPlan(DistributionPlan distributionPlan) {
        this.distributionPlan = distributionPlan;
    }

    public Date getTakeOffDate() {
        return this.takeOffDate;
    }
    
    public void setTakeOffDate(Date takeOffDate) {
        this.takeOffDate = takeOffDate;
    }

    public String getPlanName() {
        return this.planName;
    }
    
    public void setPlanName(String planName) {
        this.planName = planName;
    }

    public int getFirstNum() {
        return this.firstNum;
    }
    
    public void setFirstNum(int firstNum) {
        this.firstNum = firstNum;
    }

    public int getEconomyNum() {
        return this.economyNum;
    }
    
    public void setEconomyNum(int economyNum) {
        this.economyNum = economyNum;
    }

    public int getFirstSaleNum() {
        return this.firstSaleNum;
    }
    
    public void setFirstSaleNum(int firstSaleNum) {
        this.firstSaleNum = firstSaleNum;
    }

    public int getEconomySaleNum() {
        return this.economySaleNum;
    }
    
    public void setEconomySaleNum(int economySaleNum) {
        this.economySaleNum = economySaleNum;
    }
   








}